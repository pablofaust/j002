void	ft_putchar(char c);

void	ft_putnbr(int n)
{
	char a;
	int i;
	i = 0;

	while (n > 10)
	{
		a = n % 10;
		a = a + '0';
		ft_putchar(a);
		i++;
		n = n / (10 ^ i);
	}
}
